char	*ft_strchr(const char *s, int c)
{
	int	i;

	if (!s && c)
		return (0);
	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
		{
			return ((char *)s + i);
		}
		i++;
	}
	if (s[i] == (char)c)
		return ((char *)s + i);
	return (0);
}
